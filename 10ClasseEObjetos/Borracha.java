package ClasseEObjetos;

public class Borracha {
    String cor;
    String marca;
    boolean capa;

    public Borracha(String cor, String marca, boolean capa) {
        this.cor = cor;
        this.marca = marca;
        this.capa = capa;
    }

    public String getCor(){
        return cor;
    }
    
}
